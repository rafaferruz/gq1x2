package com.gq2.domain;

/**
 *
 * @author RAFAEL FERRUZ
 */
public class Setup {
    private Integer stpId;
    private String stpSection;
    private String stpParameter;
    private String stpValue;

    public Setup() {
    }

    public Integer getStpId() {
	return stpId;
    }

    public void setStpId(Integer stpId) {
	this.stpId = stpId;
    }

    public String getStpSection() {
	return stpSection;
    }

    public void setStpSection(String stpSection) {
	this.stpSection = stpSection;
    }

    public String getStpParameter() {
	return stpParameter;
    }

    public void setStpParameter(String stpParameter) {
	this.stpParameter = stpParameter;
    }

    public String getStpValue() {
	return stpValue;
    }

    public void setStpValue(String stpValue) {
	this.stpValue = stpValue;
    }
    
}
